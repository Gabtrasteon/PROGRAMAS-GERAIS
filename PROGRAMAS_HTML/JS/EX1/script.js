var n1 = Number(prompt(`Digite um valor`))
var n2 = Number(prompt(`Digite outro valor`))
var soma = n1+n2
var menos = n1-n2
var mult = n1*n2
var div = n1/n2
var expo = n1**n2
var rest = n1%n2
alert(`${n1} + ${n2} = ${soma}`)

alert(`${n1} - ${n2} = ${menos}`)

alert(`${n1} x ${n2} = ${mult}`)

alert(`${n1} / ${n2} = ${div}`)

alert(`${n1} ^ ${n2} = ${expo}`)

alert(`${n1} % ${n2} = ${rest}`)